function getData(city, year) {
  console.log(city, year);

  $.ajax({
    url:
      "https://www.ncdc.noaa.gov/cdo-web/api/v2/data?datasetid=GHCND&datatypeid=TMAX&locationid=" +
      city +
      "&startdate=" +
      year +
      "-01-01&enddate=" +
      year +
      "-12-31&limit=1",
    headers: { token: noaaToken },
  })
    .then(function (response) {
      return response.results;
    })
    .then(function (data) {
      return data[0].station;
    })
    .then(function (station) {
      $.ajax({
        url:
          "https://www.ncdc.noaa.gov/cdo-web/api/v2/data?datasetid=GHCND&datacategoryid=TEMP&datatypeid=TMAX&stationid=" +
          station +
          "&startdate=2020-01-01&enddate=2020-12-31&limit=1000&units=metric",
        headers: { token: noaaToken },
      })
        .then(function (response) {
          return response.results;
        })
        .then(function (tempData) {
          let tbody = document.getElementById("tbody");

          for (let i = 0; i < tempData.length; i++) {
            let tr = "<tr>";
            tr +=
              "<td>" +
              tempData[i].date +
              "</td>" +
              "<td>" +
              tempData[i].value +
              "</td></tr>";
            tbody.innerHTML += tr;
          }
        });
    });
}

